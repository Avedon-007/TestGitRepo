package lesson2.part1;

public class Human {
	private double humanHeight;
	protected String humanName;
	private double humanWeight;
	private String humanEyesColor;
	protected String humanCountry;
	
	public double getHumanHeight() {
		return humanHeight;
	}
	public void setHumanHeight(double humanHeight) {
		this.humanHeight = humanHeight;
	}
	public String getHumanName() {
		return humanName;
	}
	public void setHumanName(String humanName) {
		this.humanName = humanName;
	}
	public double getHumanWeight() {
		return humanWeight;
	}
	public void setHumanWeight(double humanWeight) {
		this.humanWeight = humanWeight;
	}
	public String getHumanEyesColor() {
		return humanEyesColor;
	}
	public void setHumanEyesColor(String humanEyesColor) {
		this.humanEyesColor = humanEyesColor;
	}
	public String getHumanCountry() {
		return humanCountry;
	}
	public void setHumanCountry(String humanCountry) {
		this.humanCountry = humanCountry;
	}
	
	
}
